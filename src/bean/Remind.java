package bean;

public class Remind {
	private int id;
	private int remindId;
	private boolean isMes;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getRemindId() {
		return remindId;
	}
	public void setRemindId(int remindId) {
		this.remindId = remindId;
	}
	public boolean isMes() {
		return isMes;
	}
	public void setMes(boolean isMes) {
		this.isMes = isMes;
	}
	
}
